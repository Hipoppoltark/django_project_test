// LOGOUT USER
export const logout = () => (dispatch, getState) => {
  axios
    .post('/api/auth/logout/', null, tokenConfig(getState, 'refresh'))
    .then((res) => {
      dispatch({
        type: LOGOUT_SUCCESS,
      });
    })
    .catch((err) => {
      dispatch(returnErrors(err.response.data, err.response.status));
    });
};

// Setup config with token - helper function
export const tokenConfig = (getState, token) => {
  // Headers
  const config = {
    headers: {
      'Content-Type': 'application/json',
    }
  };

  const access_token = getState().auth.access;
  if (access_token){
    config.headers['Authorization'] = `Bearer ${access_token}`;
    config.headers['refresh'] = getState().auth.refresh;
    if (token == 'refresh') {
      config.data['refresh'] = getState().auth.refresh;
    }
  }


  return config;
};